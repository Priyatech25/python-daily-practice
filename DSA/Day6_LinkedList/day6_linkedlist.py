# DAY 6 - DSA Practice (Linked List)

# Node class
class Node:
    def __init__(self, data):
        self.data = data
        self.next = None


# Linked List class
class LinkedList:
    def __init__(self):
        self.head = None

    # Insert at end
    def insert(self, data):
        new_node = Node(data)

        if self.head is None:
            self.head = new_node
            return

        temp = self.head
        while temp.next:
            temp = temp.next

        temp.next = new_node

    # Display Linked List
    def display(self):
        temp = self.head
        while temp:
            print(temp.data, end=" -> ")
            temp = temp.next
        print("None")

    # Search element
    def search(self, key):
        temp = self.head
        position = 0

        while temp:
            if temp.data == key:
                return position
            temp = temp.next
            position += 1

        return -1


# Testing
ll = LinkedList()

ll.insert(10)
ll.insert(20)
ll.insert(30)
ll.insert(40)

print("Linked List:")
ll.display()

print("Search 30:", ll.search(30))
print("Search 100:", ll.search(100))